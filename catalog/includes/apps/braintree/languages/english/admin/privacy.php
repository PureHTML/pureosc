privacy_body = <h3>Braintree Modules</h3>

<p>Braintree modules send store owner, online store, and customer related information to Braintree to process API transactions. These include the following modules:</p>

<ul>
  <li>Braintree Credit Cards</li>
</ul>

<p>The following information is included in API calls sent to Braintree:</p>

<ul>
  <li>Braintree merchant account information of the seller / store owner.</li>
  <li>Customer shipping and billing addresses.</li>
  <li>The order total and currency.</li>
  <li>E-Commerce solution identification.</li>
</ul>

<h3>App Updates</h3>

<p>The Braintree App for osCommerce Online Merchant automatically checks the osCommerce website for updates that are available to the App. This check is performed once every 24 hours and if an update is available, a notification is shown to allow the App to download and apply the update.</p>

<p>A manual check for available updates is also performed on the Braintree App Info page.</p>

<h3>Google Hosted Libraries (jQuery and jQuery UI)</h3>

<p>If jQuery or jQuery UI are not already loaded in the Shop Frontend or Administration Tool, the Braintree App automatically loads the libraries securely through Google Hosted Libraries.</p>
